-- DELETE FROM second_table``: Esta parte de la instrucción indica que se van a eliminar filas de la tabla llamada second_table.
-- WHERE score <= 5;: Esta cláusula especifica la condición que deben cumplir las filas para ser eliminadas. En este caso, la instrucción eliminará las filas donde el valor en la columna score sea menor o igual a 5.
DELETE FROM second_table
WHERE score <= 5;
